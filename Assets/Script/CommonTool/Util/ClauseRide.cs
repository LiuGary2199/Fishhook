using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ClauseRide
{
    public static string PhraseIDPer(double a)
    {
        return Math.Round(a, PotionMillet.DimlyVastly).ToString();
    }
    public static string PhraseIDPer(double a, int digits)
    {
        return Math.Round(a, digits).ToString();
    }

    public static double Dimly(double a)
    {
        return Math.Round(a, PotionMillet.DimlyVastly);
    }

}
